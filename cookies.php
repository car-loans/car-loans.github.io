        <h4>What Are Cookies?</h4>
		<p>A cookie is a small text file that a website saves on your computer
 or mobile device when you visit the site. It enables the website to 
remember your actions and preferences (such as login, language, font 
size and other display preferences) over a period of time, so you don't 
have to keep re-entering them whenever you come back to the site or 
browse from one page to another.</p>
		
		<p>Cookies are small text files that are placed on your computer's 
hard drive by websites that you visit.  They save and retrieve pieces of
 information about your visit to the website. For example, how you 
entered the site, how you navigated through the site and what 
information and documentation was of interest to you. This means that 
when you go back to a website, it can often give you tailored options 
based on the information it has stored about you on your last visit.</p>
		
